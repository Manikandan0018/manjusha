// models/AdminProduct.js
import mongoose from "mongoose";

const ProductSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: String,
    required: true,
  },
  description: {
    type: String,
  },
  image: {
    type: String,
    required: true,
  },
}, { timestamps: true });

export default mongoose.model("CartProduct", ProductSchema);
